package modelos;

public class Boleto {
    private int codigoDeBoleto;
    private int nitCliente;
    private int codigoAsiente;
    private int precioBoleto;
    private int idListaBoleto;

    public int getCodigoDeBoleto() {
        return codigoDeBoleto;
    }

    public void setCodigoDeBoleto(int codigoDeBoleto) {
        this.codigoDeBoleto = codigoDeBoleto;
    }

    public int getNitCliente() {
        return nitCliente;
    }

    public void setNitCliente(int nitCliente) {
        this.nitCliente = nitCliente;
    }

    public int getCodigoAsiente() {
        return codigoAsiente;
    }

    public void setCodigoAsiente(int codigoAsiente) {
        this.codigoAsiente = codigoAsiente;
    }

    public int getPrecioBoleto() {
        return precioBoleto;
    }

    public void setPrecioBoleto(int precioBoleto) {
        this.precioBoleto = precioBoleto;
    }

    public int getIdListaBoleto() {
        return idListaBoleto;
    }

    public void setIdListaBoleto(int idListaBoleto) {
        this.idListaBoleto = idListaBoleto;
    }
    
}
